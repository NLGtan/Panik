import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptFile = fileURLToPath(import.meta.url);
const scriptDir = path.dirname(scriptFile);
const frontendRoot = path.resolve(scriptDir, "..");
const repoRoot = path.resolve(frontendRoot, "..");

const deploymentPath = path.join(repoRoot, "deploy", "addresses.base-sepolia.json");
const panikAbiPath = path.join(repoRoot, "abi", "PanikExecutor.json");

if (!fs.existsSync(deploymentPath)) {
  throw new Error(`Missing deployment file: ${deploymentPath}`);
}

if (!fs.existsSync(panikAbiPath)) {
  throw new Error(`Missing ABI file: ${panikAbiPath}. Run 'npm run export:abi' in repo root.`);
}

const deployment = JSON.parse(fs.readFileSync(deploymentPath, "utf8"));
const panikAbi = JSON.parse(fs.readFileSync(panikAbiPath, "utf8"));

const outConfigDir = path.join(frontendRoot, "src", "config");
const outAbiDir = path.join(frontendRoot, "src", "abi");

fs.mkdirSync(outConfigDir, { recursive: true });
fs.mkdirSync(outAbiDir, { recursive: true });

const generatedConfigTs = `// Auto-generated by scripts/sync-onchain-config.mjs
// Do not edit manually.
export const onchainConfig = {
  chainId: ${Number(deployment.chainId)},
  panikExecutor: "${deployment.addresses.panikExecutor}",
  lockChecker: "${deployment.addresses.lockChecker}",
  usdc: "${deployment.config.usdc}",
} as const;
`;

fs.writeFileSync(path.join(outConfigDir, "generated.ts"), generatedConfigTs);
fs.writeFileSync(path.join(outAbiDir, "PanikExecutor.json"), JSON.stringify(panikAbi, null, 2));

console.log("Synced on-chain config and ABI into frontend/src.");
